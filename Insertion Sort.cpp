//Insertion sort function

int insertion (int array[], int arraySize)
{
    int i,j,key;
    for(int i=1;i<arraySize; i++)
    {
        key=array[i];
        j=i-1;
        while(j>=0 && array[j]>key)
        {
            array[j+1]=array[j];
            j=j-1;
        }
        array[j+1]=key;
    }
}
